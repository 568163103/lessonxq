package com.xq.domain;

public class IconMap {
    private Long iconmapid;

    private String name;

    private Long defaultIconid;

    public Long getIconmapid() {
        return iconmapid;
    }

    public void setIconmapid(Long iconmapid) {
        this.iconmapid = iconmapid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getDefaultIconid() {
        return defaultIconid;
    }

    public void setDefaultIconid(Long defaultIconid) {
        this.defaultIconid = defaultIconid;
    }
}