package com.xq.domain;

public class SysmapsLinkTriggers {
    private Long linktriggerid;

    private Long linkid;

    private Long triggerid;

    private Integer drawtype;

    private String color;

    public Long getLinktriggerid() {
        return linktriggerid;
    }

    public void setLinktriggerid(Long linktriggerid) {
        this.linktriggerid = linktriggerid;
    }

    public Long getLinkid() {
        return linkid;
    }

    public void setLinkid(Long linkid) {
        this.linkid = linkid;
    }

    public Long getTriggerid() {
        return triggerid;
    }

    public void setTriggerid(Long triggerid) {
        this.triggerid = triggerid;
    }

    public Integer getDrawtype() {
        return drawtype;
    }

    public void setDrawtype(Integer drawtype) {
        this.drawtype = drawtype;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}