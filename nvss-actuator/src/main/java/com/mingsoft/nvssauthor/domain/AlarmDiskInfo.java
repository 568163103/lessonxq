package com.mingsoft.nvssauthor.domain;

public class AlarmDiskInfo {
    private Integer id;

    /**
     * 机器IP
     */
    private String ip;

    /**
     * 已使用容量
     */
    private Integer diskCapacityUsed;

    /**
     * 总容量
     */
    private Integer diskCountCapacity;

    /**
     * 总硬盘数
     */
    private Integer diskCount;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public Integer getDiskCapacityUsed() {
        return diskCapacityUsed;
    }

    public void setDiskCapacityUsed(Integer diskCapacityUsed) {
        this.diskCapacityUsed = diskCapacityUsed;
    }

    public Integer getDiskCountCapacity() {
        return diskCountCapacity;
    }

    public void setDiskCountCapacity(Integer diskCountCapacity) {
        this.diskCountCapacity = diskCountCapacity;
    }

    public Integer getDiskCount() {
        return diskCount;
    }

    public void setDiskCount(Integer diskCount) {
        this.diskCount = diskCount;
    }
}