package com.xq.domain;

public class ActionType {
    /**
    * action type value
    */
    private String type;

    /**
    * action name
    */
    private String name;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}